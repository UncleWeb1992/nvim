-- ============================
-- НАСТРОЙКИ ПОИСКА
-- ============================

-- Игнорировать регистр при поиске (например, "СЛОВО" = "слово" = "Слово")
vim.opt.ignorecase = true

-- Умный регистр: если в поисковом запросе есть заглавные буквы,
-- поиск становится чувствительным к регистру
-- Пример: "Word" ≠ "word", но "word" = "Word" = "WORD"
vim.opt.smartcase = true

-- ============================
-- НАСТРОЙКИ МЫШИ
-- ============================

-- Включить поддержку мыши во всех режимах:
-- n - нормальный режим
-- v - визуальный режим
-- i - режим вставки
-- c - командный режим
-- a - все режимы (сокращение для nvicha)
vim.opt.mouse = "a"

-- Автоматически переключать фокус на окно под курсором мыши
vim.opt.mousefocus = true

-- ============================
-- НАСТРОЙКИ ОТОБРАЖЕНИЯ СТРОК
-- ============================

-- Показывать абсолютные номера строк (1, 2, 3...)
vim.opt.number = true

-- Показывать относительные номера строк (расстояние от текущей строки)
-- Удобно для команд типа "d5j" (удалить 5 строк вниз)
vim.opt.relativenumber = true

-- ============================
-- ПРОВЕРКА ОРФОГРАФИИ
-- ============================

-- Языки для проверки орфографии
-- Будет подчеркивать ошибки на русском и английском
vim.opt.spelllang = { "ru", "en" }
-- Чтобы включить проверку орфографии, добавьте: vim.opt.spell = true

-- ============================
-- НАСТРОЙКИ СКРОЛЛИНГА И ПРОКРУТКИ
-- ============================

-- Минимальное количество строк над/под курсором при скроллинге
-- Когда курсор близко к краю, окно автоматически прокручивается
vim.opt.scrolloff = 10

-- Минимальное количество колонок слева/справа от курсора
vim.opt.sidescrolloff = 8

-- Не переносить длинные строки на новую строку
-- Длинные строки будут выходить за пределы окна (горизонтальный скролл)
vim.opt.wrap = false
-- Если нужно включить перенос: vim.opt.wrap = true

-- ============================
-- НАСТРОЙКИ ТАБУЛЯЦИИ И ОТСТУПОВ
-- ============================

-- Количество пробелов, которые отображаются для символа табуляции
vim.opt.tabstop = 2

-- Количество пробелов для автоматического отступа (команды >>, <<, ==)
vim.opt.shiftwidth = 2

-- Количество пробелов для табуляции при редактировании (Backspace поведение)
vim.opt.softtabstop = 2

-- Использовать пробелы вместо символов табуляции
-- При нажатии Tab вставляются пробелы, а не символ \t
vim.opt.expandtab = true

-- Умное автоматическое отступление (для C-подобных языков)
-- Автоматически добавляет отступы после {, before }, etc.
vim.opt.smartindent = true

-- Копировать отступ с текущей строки при создании новой
vim.opt.autoindent = true

-- ============================
-- ДОПОЛНИТЕЛЬНЫЕ НАСТРОЙКИ (рекомендую добавить)
-- ============================

-- Показывать позицию курсора (столбец и строку) в статусной строке
vim.opt.ruler = true

-- Время ожидания в мс для последовательных нажатий клавиш (как jj для Esc)
vim.opt.timeoutlen = 300

-- Включить подсветку синтаксиса
vim.opt.syntax = "enable"

-- Показывать незавершенные команды в статусной строке
vim.opt.showcmd = true

-- Подсвечивать соответствующую скобку при наведении
vim.opt.showmatch = true

-- Время подсветки соответствующей скобки (в десятых секунды)
vim.opt.matchtime = 2

-- Подсвечивать все совпадения при поиске
vim.opt.hlsearch = true

-- Автоматически читать файл, если он был изменен извне
vim.opt.autoread = true

-- Включить 256 цветов в терминале
vim.opt.termguicolors = true

-- Не создавать файлы резервных копий (можно заменить на что-то вроде undotree)
vim.opt.backup = false
vim.opt.writebackup = false
vim.opt.swapfile = false

-- Увеличить историю команд и undo
vim.opt.history = 1000
vim.opt.undolevels = 1000
